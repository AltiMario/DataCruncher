package com.seer.datacruncher.profiler.dto;

import java.io.Serializable;
import java.util.ArrayList;

public class TreeDTO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2831311034137035919L;
	private String id;
	private String text;
	private boolean leaf;
	private String cls;
	private ArrayList<TreeDTO> children;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public boolean isLeaf() {
		return leaf;
	}

	public void setLeaf(boolean leaf) {
		this.leaf = leaf;
	}

	public String getCls() {
		return cls;
	}

	public void setCls(String cls) {
		this.cls = cls;
	}

	public ArrayList<TreeDTO> getChildren() {
		return children;
	}

	public void setChildren(ArrayList<TreeDTO> children) {
		this.children = children;
	}

}
